class Patient {
    // Instance variables to store patientID, name, and age of the patient & his disease //
    String patientID;
    String name;
    int age;
    String disease;

    // Constructor to initialize a Patient //
    public Patient(String patientID, String name, int age,String disease) {
        this.patientID = patientID;
        this.name = name;
        this.age = age;
        this.disease = disease;
    }
}
